// 获取所有标签
function getAllTags() {
    const tags = new Map();
    blogData.articles.forEach(article => {
        article.tags.forEach(tag => {
            tags.set(tag, (tags.get(tag) || 0) + 1);
        });
    });
    return Array.from(tags.entries()).sort((a, b) => b[1] - a[1]);
}

// 根据标签筛选文章
function getArticlesByTag(tag) {
    return blogData.articles.filter(article => 
        article.tags.includes(tag)
    );
}

// 搜索文章
function searchArticles(keyword) {
    const lowerKeyword = keyword.toLowerCase();
    return blogData.articles.filter(article => 
        article.title.toLowerCase().includes(lowerKeyword) ||
        article.excerpt.toLowerCase().includes(lowerKeyword) ||
        article.tags.some(tag => tag.toLowerCase().includes(lowerKeyword))
    );
}

// 根据ID获取文章
function getArticleById(id) {
    return blogData.articles.find(article => article.id === parseInt(id));
}
